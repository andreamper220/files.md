package stt

import (
	"os"
	"testing"
)

func TestTranscribeIntegration(t *testing.T) {
	apiKey := os.Getenv("KIE_API_KEY")
	if apiKey == "" {
		t.Skip("KIE_API_KEY not set")
	}

	audio := []byte{
		0x4f, 0x67, 0x67, 0x53, 0x00, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x1e,
		0x01, 0x76, 0x6f, 0x72, 0x62, 0x69, 0x73, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
	}

	_, err := Transcribe(apiKey, audio, "audio/ogg", "ru")
	if err != nil {
		// Empty/silent audio may fail at poll with empty transcript; upload+createTask must not 500.
		if isRetryableSTTError(err) {
			t.Fatalf("kie create task still failing: %v", err)
		}
		t.Logf("transcribe ended without hard 500 (acceptable for silent probe audio): %v", err)
	}
}
